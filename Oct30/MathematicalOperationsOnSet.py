
# Union Operation
x = {10, 20, 30, 40}
y = {11, 12, 13, 14, 20, 30}
u = x.union(y)
print(u)

# Intersection Operation
x = {10, 20, 30, 40}
y = {11, 12, 13, 14, 20, 30}
i = x.intersection(y)
print(i)

# Difference Operation
x = {10, 20, 30, 40}
y = {11, 12, 13, 14, 20, 30}
d = x.difference(y)
print(d)

# Symmetric Difference
x = {10, 20, 30, 40}
y = {11, 12, 13, 14, 20, 30}
s = x.symmetric_difference(y)
print(s)
